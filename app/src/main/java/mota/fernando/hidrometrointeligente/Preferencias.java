package mota.fernando.hidrometrointeligente;

public class Preferencias {

    private int fluxoAgua;
    private int inteligenciaArtificial;

    public Preferencias() {}

    public int getFluxoAgua() {
        return fluxoAgua;
    }

    public void setFluxoAgua(int fluxoAgua) {
        this.fluxoAgua = fluxoAgua;
    }

    public int getInteligenciaArtificial() {
        return inteligenciaArtificial;
    }

    public void setInteligenciaArtificila(int inteligenciaArtificila) {
        this.inteligenciaArtificial = inteligenciaArtificila;
    }
}
