package mota.fernando.hidrometrointeligente;

public class Consumo {

    public long xvalue;
    public int yvalue;

    public void Consumo(){}

    public long getxvalue() {
        return xvalue;
    }

    public void setxvalue(long xvalue) {
        this.xvalue = xvalue;
    }

    public int getyvalue() {
        return yvalue;
    }

    public void setyvalue(int yvalue) {
        this.yvalue = yvalue;
    }
}
